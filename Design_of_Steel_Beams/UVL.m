% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
w1=str2double(get(handles.edit1,'String'));
w2=str2double(get(handles.edit2,'String'));
a=str2double(get(handles.edit3,'String'));
b=str2double(get(handles.edit4,'String'));

setappdata(0,'w1value',w1);
setappdata(0,'w2value',w2);
setappdata(0,'avalue',a);
setappdata(0,'bvalue',b);

if(w1<=w2)
i=4;
end

if(w1>=w2)
i=5;
end

setappdata(0,'Ivaluerepeat',i);
Trying;
close UVL;



